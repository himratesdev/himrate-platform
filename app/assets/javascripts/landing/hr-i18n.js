/* ---- self-contained i18n fallback ----
   On pages that load hr-shared.js (02–05), that script owns the RU/EN switch.
   This block only activates on pages WITHOUT it (e.g. 01-glavnaya) — it builds
   the same RU/EN toggle in the header and swaps leaf-node text. */
(function(){
  var RE=/[А-Яа-яЁё]/;
  function $all(s,r){ return Array.prototype.slice.call((r||document).querySelectorAll(s)); }
  function getLang(){ try{ return localStorage.getItem('hr-lang')||'ru'; }catch(e){ return 'ru'; } }
  function apply(lang){
    var T=window.HR_TRANS||{};
    $all('*').forEach(function(el){
      if(el.children.length) return;
      var tag=el.tagName; if(tag==='SCRIPT'||tag==='STYLE'||tag==='SVG'||tag==='PATH') return;
      var ru=el.getAttribute&&el.getAttribute('data-i18n-ru');
      if(ru==null){
        var cur=el.textContent; if(!cur||!RE.test(cur)) return;
        ru=cur.replace(/\s+/g,' ').trim();
        try{ el.setAttribute('data-i18n-ru',ru); el.setAttribute('data-i18n-o',cur); }catch(e){ return; }
      }
      if(lang==='en'){ var en=T[ru]; if(en!=null && el.textContent!==en) el.textContent=en; }
      else { var o=el.getAttribute('data-i18n-o'); if(o!=null && el.textContent!==o) el.textContent=o; }
    });
    try{ document.documentElement.setAttribute('lang', lang==='en'?'en':'ru'); }catch(e){}
    $all('[data-hr-lang-btn]').forEach(function(b){ var on=b.getAttribute('data-hr-lang-btn')===lang; b.style.background=on?'#FFFFFF1A':'transparent'; b.style.color=on?'#F5F2EC':'#8E8A9A'; });
  }
  function buildSwitch(){
    var actions=document.querySelector('[data-pencil-name="Actions"]');
    if(!actions || actions.querySelector('[data-hr-lang]')) return;
    var sw=document.createElement('div');
    sw.setAttribute('data-hr-lang','');
    sw.className='box-border w-fit shrink-0 h-fit flex flex-row items-center';
    sw.style.cssText='border:1px solid #FFFFFF24;border-radius:6px;overflow:hidden';
    ['ru','en'].forEach(function(lg){
      var t=document.createElement('div');
      t.setAttribute('data-hr-lang-btn',lg); t.textContent=lg.toUpperCase();
      t.style.cssText='padding:8px 9px;font-family:Geist Mono,monospace;font-size:11.5px;font-weight:600;letter-spacing:1px;cursor:pointer;color:#8E8A9A';
      t.addEventListener('click', function(){ window.__hrSetLang(lg); });
      sw.appendChild(t);
    });
    actions.insertBefore(sw, actions.firstChild);
  }
  function boot(){
    // hr-shared.js builds [data-hr-lang-btn]; if present, it owns the switch.
    if(document.querySelector('[data-hr-lang-btn]')) return;
    if(!window.__hrSetLang){
      window.__hrSetLang=function(l){ l=(l==='en')?'en':'ru'; try{ localStorage.setItem('hr-lang',l); }catch(e){} apply(l); };
    }
    buildSwitch();
    apply(getLang());
    setTimeout(function(){ if(!document.querySelector('[data-hr-lang-btn]')) return; apply(getLang()); }, 600);
    setTimeout(function(){ apply(getLang()); }, 1600);
  }
  if(document.readyState==='complete'||document.readyState==='interactive') setTimeout(boot, 900);
  else window.addEventListener('DOMContentLoaded', function(){ setTimeout(boot, 900); });
})();
